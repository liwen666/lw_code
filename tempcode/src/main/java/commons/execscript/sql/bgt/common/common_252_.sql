BEGIN
EXECUTE IMMEDIATE Q'{CREATE OR REPLACE VIEW CODE_T_AGENCY_BGT AS
SELECT GUID,
       CODE,
       '[' || CODE || ']' || NAME NAME,
       (CASE
         WHEN CODE = GLOBAL_MULTYEAR_CZ.SECU_F_GLOBAL_PARM('DIVID') THEN
          '0'
         ELSE
          SUPERGUID
       END) AS SUPERGUID,
       ISLEAF,
       LEVELNO,
       STATUS,
       DIVTYPE,
       ISDISTRICT,
       DISTRICTID,
       GUID AS ID,
       ISLEAF AS ENDFLAG,
       AGENCYTYPE,
       DISTRICTLVL,
       LVLID,
       CASE
         WHEN SUPERGUID = '0' THEN
          '#'
         ELSE
          NVL(SUPERGUID, '#')
       END SUPERID,
       CODE AS SHOWID,
	   FININTORG
  FROM CODE_M_AGENCY Y
 WHERE PROVINCE = GLOBAL_MULTYEAR_CZ.SECU_F_GLOBAL_PARM('DIVID')
   AND YEAR = GLOBAL_MULTYEAR_CZ.SECU_F_GLOBAL_PARM('YEAR')
   AND STATUS = '1'}';
