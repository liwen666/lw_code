CREATE OR REPLACE TRIGGER TR_P#BGT_T_ZCXMMXB_BGTSTOP
BEFORE INSERT OR UPDATE ON  P#BGT_T_ZCXMMXB FOR EACH ROW
DECLARE
  V_CNT NUMBER;
BEGIN
IF UPDATING THEN
  IF UPDATING('NEEDUPDATE') THEN
    SELECT COUNT(1) INTO V_CNT FROM EFM_T_BGTFLAG S WHERE S.BGTTYPE = '0' AND ISSTOP = '1';
    IF  (V_CNT > 0 AND :NEW.ISADJUST = '0') THEN
      :NEW.NEEDUPDATE:='';
    END IF;
  END IF;
END IF;
END TR_P#P#BGT_T_ZCXMMXB_BGTSTOP;--TR_P#BGT_T_ZCXMMXB_BGTSTOP
