CREATE OR REPLACE VIEW BGT_V_AGENCYLEVEL AS 
  SELECT '0' CODE, '退回上一步' NAME FROM DUAL
  UNION ALL 
  SELECT '1' CODE, '财政' NAME FROM DUAL  WHERE PKG_CONSTANTS.GETDEFAULTPROVINCE <> '8700'
  UNION ALL 
  SELECT '2' CODE, '部门' NAME FROM DUAL  WHERE PKG_CONSTANTS.GETDEFAULTPROVINCE <> '8700'
  UNION ALL 
  SELECT '3' CODE, '二级单位' NAME FROM DUAL  WHERE PKG_CONSTANTS.GETDEFAULTPROVINCE <> '8700'
  UNION ALL 
  SELECT '4' CODE, '三级单位' NAME FROM DUAL  WHERE PKG_CONSTANTS.GETDEFAULTPROVINCE <> '8700'
  UNION ALL 
  SELECT '9' CODE, '末级单位' NAME FROM DUAL  WHERE PKG_CONSTANTS.GETDEFAULTPROVINCE <> '8700'
